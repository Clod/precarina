SHS exposure in children. It can cause respiratory infections, ear
infections, and asthma attacks. In babies, can cause sudden infant
death syndrome (SIDS). Although public health programs have led to a
substantial decrease in the prevalence of tobacco smoking, the
adverse health effects of SHS exposure are by no means a thing of the
